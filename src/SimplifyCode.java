public class SimplifyCode {
    public static void main(String[] args) {

        int num1 = 1;
        int num2 = 2;
        int ans = num1 + num2;
        System.out.println(ans);

        num1 = num1 + 3;
        System.out.println(num1);

        num1 += 4;
        System.out.println(num1);

        num1++;
        System.out.println(num1);

    }
}
