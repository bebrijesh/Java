public class Datatype {
    public static void main(String[] args) {
        /*
            # What is Datatype?
            - There are two type of Datatype:
            - 1. Primitive data types - includes byte, short, int, long, float, double, boolean and char.
            - 2. Non-primitive data types - String, Arrays and Classes.
         */
        byte myByte = 1;
        short myShort = 10;
        int myInt = 100;
        long myLong = 1000;
        float myFloat = 9.9f;
        float myFloat2 = (float) 9.9;
        double myDouble = 99.99;
        boolean myBoolean = true;
        char myChar = '@';
        char myChar2 = '\u00A9'; // You can also store different type of UNICODE value in char.
        String myString = "ABC";
    }
}
